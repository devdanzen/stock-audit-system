    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = txtCentralKitchen.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Category Name wajib diisi");
            return;
        }
        String sql = "INSERT INTO category (name, description) VALUES (?, ?)";
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, name);
            ps.setString(2, "");
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            kosong();
            datatable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data gagal disimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (selectedId < 0) {
            JOptionPane.showMessageDialog(this, "Pilih baris dulu untuk update");
            return;
        }
        String name = txtCentralKitchen.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Category Name wajib diisi");
            return;
        }
        String sql = "UPDATE category SET name=? WHERE id=?";
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            ps.setString(1, name);
            ps.setInt(2, selectedId);
            int n = ps.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                kosong();
                datatable();
            } else {
                JOptionPane.showMessageDialog(this, "Category tidak ditemukan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data gagal diubah: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

