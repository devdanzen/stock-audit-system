    private void datatable() {
        if (conn == null) return;
        String[] cols = {"ID", "Name", "Description"};
        DefaultTableModel model = new DefaultTableModel(cols, 0);
        try (Statement st = conn.createStatement();
             ResultSet rs = st.executeQuery("SELECT id, name, description FROM category ORDER BY id")) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("name"),
                    rs.getString("description")
                });
            }
            jTableCategory.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load tabel: " + e.getMessage());
        }
    }

    private void tableMouseClicked() {
        int row = jTableCategory.getSelectedRow();
        if (row < 0) return;
        Object idObj = jTableCategory.getValueAt(row, 0);
        selectedId = (idObj instanceof Integer) ? (Integer) idObj : Integer.parseInt(String.valueOf(idObj));
        txtCentralKitchen.setText(String.valueOf(jTableCategory.getValueAt(row, 1)));
    }

