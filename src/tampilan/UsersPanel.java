    private void tableListener(){

        tableUser.getSelectionModel().addListSelectionListener(e -> {

            int row = tableUser.getSelectedRow();

            if (row != -1 && model.getValueAt(row, 0) != null) {

                selectedUserId = Integer.parseInt(
                        model.getValueAt(row, 0).toString()
                );

                txtUsername.setText(
                        model.getValueAt(row, 1).toString()
                );

                txtRole.setSelectedItem(
                        model.getValueAt(row, 2).toString()
                );

                String outletCode =
                        model.getValueAt(row, 3).toString();

                for (int i = 0; i < txtOutlets.getItemCount(); i++) {

                    ComboItem item = txtOutlets.getItemAt(i);

                    if (item.getLabel().equals(outletCode)) {

                        txtOutlets.setSelectedIndex(i);

                        break;
                    }
                }

                txtStatus.setSelectedItem(
                        model.getValueAt(row, 6).toString()
                );
            }
        });
    }
