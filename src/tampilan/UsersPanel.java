    private void addUser(){

        String username = txtUsername.getText().trim();

        String password = new String(txtPassword.getPassword());

        String confirmPassword = new String(
                txtConfirmPassword.getPassword()
        );

        String role = txtRole.getSelectedItem().toString();

        ComboItem selectedOutlet =
                (ComboItem) txtOutlets.getSelectedItem();

        int outletId = selectedOutlet.getValue();

        String status = txtStatus.getSelectedItem().toString();

        if (username.isEmpty()
                || password.isEmpty()
                || confirmPassword.isEmpty()) {

            JOptionPane.showMessageDialog(this,
                    "Semua field wajib diisi");

            return;
        }

        if (!password.equals(confirmPassword)) {

            JOptionPane.showMessageDialog(this,
                    "Confirm password tidak sama");

            return;
        }

        String sql = """
                     INSERT INTO user
                     (
                        username,
                        password_hash,
                        role,
                        outlet_id,
                        status
                     )
                     VALUES (?, ?, ?, ?, ?)
                     """;

        try (

                Connection conn = Koneksi.connect();
                PreparedStatement ps = conn.prepareStatement(sql);

                ) {

            String hashedPassword =
                    BCrypt.hashpw(password, BCrypt.gensalt());

            ps.setString(1, username);
            ps.setString(2, hashedPassword);
            ps.setString(3, role);
            ps.setInt(4, outletId);
            ps.setString(5, status);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(this,
                    "User berhasil ditambahkan");

            clearForm();
            loadData();

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(this,
                    "Tambah user gagal : " + e.getMessage());
        }
}

    private void updateUser(){

        if (selectedUserId == -1) {

            JOptionPane.showMessageDialog(this,
                    "Pilih data terlebih dahulu");

            return;
        }

        ComboItem selectedOutlet =
                (ComboItem) txtOutlets.getSelectedItem();

        int outletId = selectedOutlet.getValue();

        String sql = """
                     UPDATE user
                     SET
                        username = ?,
                        role = ?,
                        outlet_id = ?,
                        status = ?
                     WHERE user_id = ?
                     """;

        try (

                Connection conn = Koneksi.connect();
                PreparedStatement ps = conn.prepareStatement(sql);

                ) {

            ps.setString(1, txtUsername.getText());
            ps.setString(2, txtRole.getSelectedItem().toString());
            ps.setInt(3, outletId);
            ps.setString(4, txtStatus.getSelectedItem().toString());
            ps.setInt(5, selectedUserId);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(this,
                    "User berhasil diupdate");

            clearForm();
            loadData();

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(this,
                    "Update gagal : " + e.getMessage());
        }
    }
    
    private void deleteUser(){
        if (selectedUserId == -1) {

            JOptionPane.showMessageDialog(this,
                    "Pilih data terlebih dahulu");

            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin menghapus user?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        String sql = "DELETE FROM user WHERE user_id = ?";

        try (

                Connection conn = Koneksi.connect();
                PreparedStatement ps = conn.prepareStatement(sql);

                ) {

            ps.setInt(1, selectedUserId);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(this,
                    "User berhasil dihapus");

            clearForm();
            loadData();

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(this,
                    "Delete gagal : " + e.getMessage());
        }
    }
