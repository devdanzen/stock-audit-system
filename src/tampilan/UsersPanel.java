    private void searchData() {

       model.setRowCount(0);

       String keyword = txtSearch.getText().trim();

       String sql = """
                    SELECT
                       u.user_id,
                       u.username,
                       u.role,
                       o.outlet_code,
                       u.created_at,
                       u.last_login_at,
                       u.status
                    FROM user u
                    JOIN outlet o
                       ON u.outlet_id = o.outlet_id
                    WHERE
                       u.username LIKE ?
                       OR u.role LIKE ?
                       OR o.outlet_code LIKE ?
                    ORDER BY u.username
                    """;

       try (

               Connection conn = Koneksi.connect();
               PreparedStatement ps = conn.prepareStatement(sql);

               ) {

           ps.setString(1, "%" + keyword + "%");
           ps.setString(2, "%" + keyword + "%");
           ps.setString(3, "%" + keyword + "%");

           ResultSet rs = ps.executeQuery();

           while (rs.next()) {

               model.addRow(new Object[]{
                   rs.getInt("user_id"),
                   rs.getString("username"),
                   rs.getString("role"),
                   rs.getString("outlet_code"),
                   rs.getDate("created_at"),
                   rs.getTimestamp("last_login_at"),
                   rs.getString("status")
               });
           }

       } catch (SQLException e) {

           JOptionPane.showMessageDialog(this,
                   "Search gagal : " + e.getMessage());
       }
   }
    
    private void loadOutlets() {
        try {

        Connection conn = Koneksi.connect();

        String sql = """
                     SELECT
                        outlet_id,
                        outlet_code
                     FROM outlet
                     ORDER BY outlet_code
                     """;

        PreparedStatement pst = conn.prepareStatement(sql);

        ResultSet rs = pst.executeQuery();

        txtOutlets.removeAllItems();

        while (rs.next()) {

            txtOutlets.addItem(new ComboItem(
                    rs.getInt("outlet_id"),
                    rs.getString("outlet_code")
                )

            );

        }

    } catch (SQLException e) {

        System.out.println(
                "Error load outlet: " + e.getMessage()
        );
    }

   }
    
