    private void getSelectedVendorId(String code) {

        String sql = """
                     SELECT vendor_id
                     FROM vendor
                     WHERE vendor_code = ?
                     """;

        try (
            Connection conn = DBConnection.connect();
            PreparedStatement ps = conn.prepareStatement(sql)
        ) {

            ps.setString(1, code);

            ResultSet rs = ps.executeQuery();

            if (rs.next()) {

                selectedVendorId =
                        rs.getInt("vendor_id");
            }

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(this,
                    "Gagal ambil ID vendor");
        }
    }
    
