    private void loadData() {

        model.setRowCount(0);

        String sql = """
                     SELECT *
                     FROM vendor
                     ORDER BY vendor_id ASC
                     """;

        try (
            Connection conn = DBConnection.connect();
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery()
        ) {

            while (rs.next()) {

                model.addRow(new Object[]{
                    rs.getString("vendor_code"),
                    rs.getString("vendor_name"),
                    rs.getString("contact_phone"),
                    rs.getString("status")
                });
            }

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(this,
                    "Gagal load data: " + e.getMessage());
        }
    }
    

    
    private void addVendor() {

        String code = txtVendorCode.getText().trim().toUpperCase();
        String name = txtVendorName.getText().trim();
        String contact = txtContact.getText().trim();
        String status = txtStatus.getSelectedItem().toString();

        if (code.isEmpty() || name.isEmpty()) {

            JOptionPane.showMessageDialog(this,
                    "Vendor code dan vendor name wajib diisi!");
            return;
        }

        String sql = """
                     INSERT INTO vendor
                     (vendor_code, vendor_name, contact_phone, status)
                     VALUES (?, ?, ?, ?)
                     """;

        try (
            Connection conn = DBConnection.connect();
            PreparedStatement ps = conn.prepareStatement(sql)
        ) {

            ps.setString(1, code);
            ps.setString(2, name);
            ps.setString(3, contact);
            ps.setString(4, status);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(this,
                    "Vendor berhasil ditambahkan");

            loadData();
            clearForm();

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(this,
                    "Gagal tambah vendor: " + e.getMessage());
        }
    }
    
    private void updateVendor() {

        if (selectedVendorId == -1) {

            JOptionPane.showMessageDialog(this,
                    "Pilih data terlebih dahulu!");
            return;
        }

        String sql = """
                     UPDATE vendor
                     SET vendor_code = ?,
                         vendor_name = ?,
                         contact_phone = ?,
                         status = ?
                     WHERE vendor_id = ?
                     """;

        try (
            Connection conn = DBConnection.connect();
            PreparedStatement ps = conn.prepareStatement(sql)
        ) {

            ps.setString(1,
                    txtVendorCode.getText().trim().toUpperCase());

            ps.setString(2,
                    txtVendorName.getText().trim());

            ps.setString(3,
                    txtContact.getText().trim());

            ps.setString(4,
                    txtStatus.getSelectedItem().toString());

            ps.setInt(5, selectedVendorId);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(this,
                    "Vendor berhasil diupdate");

            loadData();
            clearForm();

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(this,
                    "Gagal update vendor: " + e.getMessage());
        }
    }
    
    private void deleteVendor() {

        if (selectedVendorId == -1) {

            JOptionPane.showMessageDialog(this,
                    "Pilih data terlebih dahulu!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin menghapus vendor?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        String sql = """
                     DELETE FROM vendor
                     WHERE vendor_id = ?
                     """;

        try (
            Connection conn = DBConnection.connect();
            PreparedStatement ps = conn.prepareStatement(sql)
        ) {

            ps.setInt(1, selectedVendorId);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(this,
                    "Vendor berhasil dihapus");

            loadData();
            clearForm();

        } catch (SQLException e) {

            String msg = e.getMessage() == null ? "" : e.getMessage();
            if (msg.toLowerCase().contains("foreign key")) {
                String currentStatus = txtStatus.getSelectedItem() == null
                        ? "" : txtStatus.getSelectedItem().toString();
                if ("Inactive".equalsIgnoreCase(currentStatus)) {
                    JOptionPane.showMessageDialog(this,
                            "Vendor ini masih dipakai di receiving lain, tidak bisa dihapus.\n"
                          + "Tapi statusnya sudah Inactive, jadi tidak muncul lagi di form receiving.",
                            "Tidak bisa dihapus", JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                int change = JOptionPane.showConfirmDialog(this,
                        "Vendor ini masih dipakai di receiving lain, tidak bisa dihapus.\n"
                      + "Set Status ke Inactive saja?",
                        "Tidak bisa dihapus", JOptionPane.YES_NO_OPTION);
                if (change == JOptionPane.YES_OPTION) {
                    try (Connection c2 = DBConnection.connect();
                         PreparedStatement p2 = c2.prepareStatement(
                                 "UPDATE vendor SET status='Inactive' WHERE vendor_id=?")) {
                        p2.setInt(1, selectedVendorId);
                        p2.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Status diubah ke Inactive.");
                        loadData();
                        clearForm();
                    } catch (SQLException ex2) {
                        JOptionPane.showMessageDialog(this, "Gagal: " + ex2.getMessage());
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this,
                        "Gagal hapus vendor: " + e.getMessage());
            }
        }
    }

    private void searchData() {

        model.setRowCount(0);

        String keyword = btnSearch.getText().trim();

        String sql = """
                     SELECT *
                     FROM vendor
                     WHERE vendor_code LIKE ?
                        OR vendor_name LIKE ?
                        OR contact_phone LIKE ?
                        OR status LIKE ?
                     """;

        try (
            Connection conn = DBConnection.connect();
            PreparedStatement ps = conn.prepareStatement(sql)
        ) {

            ps.setString(1, "%" + keyword + "%");
            ps.setString(2, "%" + keyword + "%");
            ps.setString(3, "%" + keyword + "%");
            ps.setString(4, "%" + keyword + "%");

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {

                model.addRow(new Object[]{
                    rs.getString("vendor_code"),
                    rs.getString("vendor_name"),
                    rs.getString("contact_phone"),
                    rs.getString("status")
                });
            }

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(this,
                    "Search gagal: " + e.getMessage());
        }
    }
    
    
    private void tableListener() {

        jTable1.getSelectionModel().addListSelectionListener(e -> {

            int row = jTable1.getSelectedRow();

            if (row != -1) {

                String code =
                        model.getValueAt(row, 0).toString();

                txtVendorCode.setText(code);

                txtVendorName.setText(
                        model.getValueAt(row, 1).toString()
                );

                txtContact.setText(
                        model.getValueAt(row, 2).toString()
                );

                txtStatus.setSelectedItem(
                        model.getValueAt(row, 3).toString()
                );

                getSelectedVendorId(code);
            }
        });
    }
