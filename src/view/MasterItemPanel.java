    private void searchData() {

        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("ID");
        model.addColumn("Item Code");
        model.addColumn("Description");
        model.addColumn("Item Class");
        model.addColumn("Category");
        model.addColumn("Outlet");
        model.addColumn("Base Unit");
        model.addColumn("Cost");
        model.addColumn("Price");
        model.addColumn("Status");

        try {

            Connection conn = DBConnection.connect();

            String sql =
                "SELECT " +
                "mi.item_id, " +
                "mi.item_code, " +
                "mi.description, " +
                "mi.item_class, " +
                "c.category_name, " +
                "o.outlet_code, " +
                "mi.base_unit, " +
                "mi.current_cost, " +
                "mi.selling_price, " +
                "mi.status " +
                "FROM master_item mi " +
                "LEFT JOIN category c ON mi.category_id = c.category_id " +
                "LEFT JOIN outlet o ON mi.outlet_id = o.outlet_id " +
                "WHERE mi.item_code LIKE ? " +
                "OR mi.description LIKE ? " +
                "ORDER BY mi.item_id DESC";

            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, "%" + txtSearch.getText() + "%");
            ps.setString(2, "%" + txtSearch.getText() + "%");

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {

                model.addRow(new Object[] {
                    rs.getInt("item_id"),
                    rs.getString("item_code"),
                    rs.getString("description"),
                    rs.getString("item_class"),
                    rs.getString("category_name"),
                    rs.getString("outlet_code"),
                    rs.getString("base_unit"),
                    rs.getDouble("current_cost"),
                    rs.getDouble("selling_price"),
                    rs.getString("status")
                });
            }

            MasterItemTable.setModel(model);
            MasterItemTable.getColumnModel().getColumn(0).setMinWidth(0);
            MasterItemTable.getColumnModel().getColumn(0).setMaxWidth(0);
            MasterItemTable.getColumnModel().getColumn(0).setWidth(0);

            MasterItemTable.getColumnModel().getColumn(0).setMinWidth(0);
            MasterItemTable.getColumnModel().getColumn(0).setMaxWidth(0);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void loadCategory() {

        txtCategory.removeAllItems();

        try {
            Connection conn = DBConnection.connect();

            String sql = "SELECT category_id, category_name FROM category ORDER BY category_name";

            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {

                txtCategory.addItem(
                    new ComboItem(
                        rs.getInt("category_id"),
                        rs.getString("category_name")
                    )
                );
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
