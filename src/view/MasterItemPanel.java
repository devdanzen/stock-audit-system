    private void tableListener() {
        MasterItemTable.getSelectionModel().addListSelectionListener(e -> {
            int row = MasterItemTable.getSelectedRow();

            if (row != -1 && model.getValueAt(row, 0) != null) {
                selectedItemId = Integer.parseInt(model.getValueAt(row, 0).toString());

                txtItemCode.setText(model.getValueAt(row, 1).toString());
                txtDesc.setText(model.getValueAt(row, 2).toString());
                txtItemClass.setSelectedItem(model.getValueAt(row, 3).toString());

                String categoryLabel = model.getValueAt(row, 4).toString();
                for (int i = 0; i < txtCategory.getItemCount(); i++) {
                    ComboItem item = (ComboItem) txtCategory.getItemAt(i);
                    if (item.getLabel().equals(categoryLabel)) {
                        txtCategory.setSelectedIndex(i);
                        break;
                    }
                }

                String outletLabel = model.getValueAt(row, 5).toString();
                for (int i = 0; i < txtOutlet.getItemCount(); i++) {
                    ComboItem item = (ComboItem) txtOutlet.getItemAt(i);
                    if (item.getLabel().equals(outletLabel)) {
                        txtOutlet.setSelectedIndex(i);
                        break;
                    }
                }

                txtBaseUnit.setSelectedItem(model.getValueAt(row, 6).toString());
                txtPurchUnit.setSelectedItem(model.getValueAt(row, 7).toString());
                txtCurrCost.setText(model.getValueAt(row, 8).toString());
                txtSellPrice.setText(model.getValueAt(row, 9).toString());
                txtStatus.setSelectedItem(model.getValueAt(row, 10).toString());
            }
        });
    }
    
    
    private void updateItem(){
        try {

            if (selectedItemId == 0) {

                JOptionPane.showMessageDialog(null, "Pilih data terlebih dahulu");
                return;
            }

            Connection conn = DBConnection.connect();

            ComboItem category = (ComboItem) txtCategory.getSelectedItem();
            ComboItem outlet = (ComboItem) txtOutlet.getSelectedItem();

            String sql =
                "UPDATE master_item SET " +
                "item_code=?, " +
                "description=?, " +
                "item_class=?, " +
                "category_id=?, " +
                "outlet_id=?, " +
                "base_unit=?, " +
                "purchase_unit=?, " +
                "current_cost=?, " +
                "selling_price=?, " +
                "status=? " +
                "WHERE item_id=?";

            PreparedStatement ps = conn.prepareStatement(sql);

            ps.setString(1, txtItemCode.getText());
            ps.setString(2, txtDesc.getText());
            ps.setString(3, txtItemClass.getSelectedItem().toString());

            ps.setInt(4, category.getValue());
            ps.setInt(5, outlet.getValue());

            ps.setString(6, txtBaseUnit.getSelectedItem().toString());
            ps.setString(7, txtPurchUnit.getSelectedItem().toString());

            ps.setDouble(8, Double.parseDouble(txtCurrCost.getText()));
            ps.setDouble(9, Double.parseDouble(txtSellPrice.getText()));

            ps.setString(10, txtStatus.getSelectedItem().toString());

            ps.setInt(11, selectedItemId);

            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil diupdate");

            loadTable();
            clearForm();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    private void deleteItem(){
        try {

            if (selectedItemId == 0) {

                JOptionPane.showMessageDialog(null, "Pilih data terlebih dahulu");
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(
                null,
                "Yakin ingin menghapus data?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
            );

            if (confirm == JOptionPane.YES_OPTION) {

                Connection conn = DBConnection.connect();

                String sql = "DELETE FROM master_item WHERE item_id=?";

                PreparedStatement ps = conn.prepareStatement(sql);

                ps.setInt(1, selectedItemId);

                ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");

                loadTable();
                clearForm();
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
